package librisuite.business.marchelper;

public class MarcHelperException extends Exception {

	public MarcHelperException() {
		super();
	}

	public MarcHelperException(String message, Throwable cause) {
		super(message, cause);
	}

	public MarcHelperException(String message) {
		super(message);
	}

	public MarcHelperException(Throwable cause) {
		super(cause);
	}

}
